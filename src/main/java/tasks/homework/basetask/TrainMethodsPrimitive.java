package tasks.homework.basetask;

public class TrainMethodsPrimitive {

    public void printInt(int x) {

        System.out.println("Я получил на вход число " + x);
    }

    public void printLong(long y) {

        System.out.println("Я получил на вход длинное число " + y);
    }

    public void printChar(char z) {
        System.out.println("Я получил на вход символ " + z);
    }

    public void printFloat(float a) {
        System.out.println("Я получил на вход дробное число " + a);
    }

    public void printDouble(double b) {
        System.out.println("Я получил на вход длинное дробное число " + b);
    }

    public void printShort(short c) {
        System.out.println("Я получил на вход короткое число " + c);
    }

    public void printByte(byte d) {
        System.out.println("Я получил на вход очень короткое число " + d);
    }

    public void printBoolean(boolean e) {
        System.out.println("Я получил на вход булево " + e);
    }
}
