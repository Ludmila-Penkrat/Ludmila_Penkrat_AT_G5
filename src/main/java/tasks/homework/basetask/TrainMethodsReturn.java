package tasks.homework.basetask;

public class TrainMethodsReturn {

    public int returnNewInt(int x) {
        return x * 3;
    }

    public long returnNewLong(long y) {
        return y - 4;
    }

    public String returnNewChar(char f) {
        return " "+f+" "+f+" ";
    }

    public float returnNewFloat(float g) {
        return g / 2;
    }

    public double returnNewDouble(double j) {
        return j + 8;
    }

    public int returnNewShort(short k) {
        return k - 1;

    }

    public int returnNewByte(byte l) {
        return l * 2;

    }

    public boolean returnNewBoolean(boolean m) {
        return !m;

    }

}
